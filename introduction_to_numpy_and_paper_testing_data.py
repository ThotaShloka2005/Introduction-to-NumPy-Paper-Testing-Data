# âœ… Step 2: Simple Summary Function
def print_summary(name, array):
    mean_val = np.mean(array)
    median_val = np.median(array)
    std_dev = np.std(array)

    print("----", name, "Statistics ----")
    print("Mean:", round(mean_val, 2))
    print("Median:", round(median_val, 2))
    print("Standard Deviation:", round(std_dev, 2))
    print("-----------------------------\n")

# âœ… Call the function for each column
